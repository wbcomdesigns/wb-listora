#!/usr/bin/env bash
# bin/run-journeys.sh — orchestrate journey audits for WB Listora.
#
# Each journey under audit/journeys/**/*.md is a self-contained
# customer-flow contract. This script enumerates them, builds a runbook
# JSON pointing at $SITE_URL, and writes results to
# audit/journey-runs/{YYYY-MM-DD-HHMM}/.
#
# Usage:
#   bash bin/run-journeys.sh                       # all journeys
#   bash bin/run-journeys.sh --site http://other   # different site
#   bash bin/run-journeys.sh --only customer/03    # one journey by slug fragment
#   bash bin/run-journeys.sh --priority critical   # filter by frontmatter priority
#   bash bin/run-journeys.sh --dry-run             # list what would run, no exec
#
# Generated by wp-plugin-onboard skill. Edit freely.

set -uo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PLUGIN_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
cd "$PLUGIN_DIR"

SITE_URL="${LOCAL_CI_SITE_URL:-http://wb-listora.local}"
ONLY=""
PRIORITY=""
DRY_RUN=0

while [ $# -gt 0 ]; do
  case "$1" in
    --site)     SITE_URL="$2"; shift ;;
    --only)     ONLY="$2"; shift ;;
    --priority) PRIORITY="$2"; shift ;;
    --dry-run)  DRY_RUN=1 ;;
    *) echo "Unknown flag: $1"; exit 2 ;;
  esac
  shift
done

# ─── Discover journeys ───────────────────────────────────────────────────────

JOURNEYS=()
while IFS= read -r -d '' f; do
  rel="${f#$PLUGIN_DIR/}"
  if [ -n "$ONLY" ] && [[ "$rel" != *"$ONLY"* ]]; then
    continue
  fi
  if [ -n "$PRIORITY" ]; then
    actual_priority=$(awk '/^priority:/ { print $2; exit }' "$f")
    [ "$actual_priority" = "$PRIORITY" ] || continue
  fi
  JOURNEYS+=("$rel")
done < <(find audit/journeys -type f -name '*.md' -not -name 'README.md' -print0 | sort -z)

if [ ${#JOURNEYS[@]} -eq 0 ]; then
  echo "No journeys matched filter (only=$ONLY priority=$PRIORITY)"
  exit 0
fi

# ─── Build the runbook ───────────────────────────────────────────────────────

RUN_ID="$(date +%Y-%m-%d-%H%M)"
RUN_DIR="audit/journey-runs/$RUN_ID"
mkdir -p "$RUN_DIR"

RUNBOOK="$RUN_DIR/runbook.json"
{
  printf '{\n'
  printf '  "run_id": "%s",\n' "$RUN_ID"
  printf '  "site": "%s",\n' "$SITE_URL"
  printf '  "started_at": "%s",\n' "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  printf '  "journeys": [\n'
  for i in "${!JOURNEYS[@]}"; do
    j="${JOURNEYS[$i]}"
    slug=$(awk '/^journey:/ { print $2; exit }' "$j")
    priority=$(awk '/^priority:/ { print $2; exit }' "$j")
    printf '    { "slug": "%s", "priority": "%s", "path": "%s" }' "$slug" "$priority" "$j"
    [ "$i" -lt $((${#JOURNEYS[@]} - 1)) ] && printf ','
    printf '\n'
  done
  printf '  ]\n}\n'
} > "$RUNBOOK"

echo "=== WB Listora journeys ==="
echo "Run ID: $RUN_ID"
echo "Site:   $SITE_URL"
echo "Found:  ${#JOURNEYS[@]} journeys"
echo ""
for j in "${JOURNEYS[@]}"; do
  slug=$(awk '/^journey:/ { print $2; exit }' "$j")
  priority=$(awk '/^priority:/ { print $2; exit }' "$j")
  printf "  [%-9s] %s  (%s)\n" "$priority" "$slug" "$j"
done
echo ""

if [ "$DRY_RUN" = "1" ]; then
  echo "Dry run — exiting before execution. Runbook at $RUNBOOK"
  exit 0
fi

# ─── Execution path ──────────────────────────────────────────────────────────
#
# Journeys are executed by an agent that has Playwright + curl + MySQL
# access. Today the integration is manual: feed the runbook to a
# claude-code session that has the relevant MCPs loaded:
#
#   claude-code "Run journeys per $RUNBOOK — for each, read the journey \
#     markdown, execute every step against $SITE_URL using the playwright \
#     MCP and curl/mysql, then write $RUN_DIR/{slug}.json with the schema \
#     in audit/journeys/README.md"
#
# When wppqa_run_journeys MCP tool ships, this script will invoke it
# directly. Until then, a regression sentinel: if the most recent prior
# run has any FAIL journeys, fail this run too.

echo "Runbook written to: $RUNBOOK"
echo ""
echo "Execute via journey-aware agent. Suggested prompt:"
echo "  Run all journeys in $RUNBOOK against $SITE_URL. For each: read"
echo "  the markdown, execute every step (Playwright + curl + mysql),"
echo "  write $RUN_DIR/{slug}.json. Aggregate to $RUN_DIR/summary.json."
echo ""

LATEST_PRIOR=$(ls -1 audit/journey-runs/ 2>/dev/null | grep -v "$RUN_ID" | tail -1 || echo "")
if [ -n "$LATEST_PRIOR" ] && [ -f "audit/journey-runs/$LATEST_PRIOR/summary.json" ]; then
  PRIOR_FAIL=$(jq -r '.failed // 0' "audit/journey-runs/$LATEST_PRIOR/summary.json" 2>/dev/null || echo "0")
  if [ "$PRIOR_FAIL" -gt 0 ]; then
    echo "Most recent prior run ($LATEST_PRIOR) had $PRIOR_FAIL failed journeys."
    echo "Run them again and ship a fix before merging."
    exit 1
  fi
fi

exit 0
